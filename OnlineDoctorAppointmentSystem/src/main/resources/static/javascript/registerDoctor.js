function validateMonday() {
		if(document.getElementById("monday").checked) {
			document.getElementById("mondayOpening").disabled = false;
			document.getElementById("mondayClosing").disabled = false;
		}
		else {
			document.getElementById("mondayOpening").disabled = true;
			document.getElementById("mondayClosing").disabled = true;
		}
			
	}
		
